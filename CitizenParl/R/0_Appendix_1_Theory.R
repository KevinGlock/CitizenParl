## Research target



The aim of the analysis is to explain potential convergences in parliamentary partiesÂ´ argumentation on citizenship through
analysing the long-term correlation between immigration rate and speech frequency on citizenship topics in German regional parliaments.


It is assumed that the immigration rate and its salience effect on speech frequency can affect parties arguemntation and that regional differences between the regional states exists
Therefore the analysis focuses on long-term frequencies of _key-word-in-contexts_ and its term _collocations_ of _ngram_-combinations are analysed
to find the significant vocabulary of parliamentary parties to compare them and identify convergences and divergences over time and between both legislative levels, federal and state.

***

Beforehand, a dictionary including relevant terms of citizenship discourse was deductively formulated.
Related to the theoretical model, the dictionary contains terms referring to actors, law/regulation, and issues of the parliamentary citizenship discourse.

Three research questions will be answered:

_To what extent has the linguistic contextualization of the concept of citizenship in plenary debates of the Federal Republic of Germany on federal and state level changed between 1990 and 2018?_

_How is the concept of dual citizenship illustrated by political actors at federal and state level?_

_In what scope do imaginations of dual citizenship differ between parliamentary groups and its speak-ing members in parliament and between both levels? Which arguments are represented?_

