## This is the annex repository belonging to my Bachelor´s thesis "The parliamentary discourse on citizenship in Germany between 1996 and 2016".
## The repository includes all relevant code to replicate the whole analysis steps.


## Research target

## The aim of the analysis is to explain potential convergences and divergences in parliamentary parties´ contextualisation on citizenship.
## It is assumed that the immigration rate and its salience effect on speech frequencies can affect parties contextualisation
## Therefore the analysis focuses on long-term frequencies of key-word-in-contexts.
## The long-term correlation between immigration rate and speech frequencies on citizenship topics in the German Bundestag is analysed.
## Further, describtive time-series is applied to get the wider contexts.
## The next part includes the focus on its collocations of ngram-combinations to find the significant part-of-speech-combinations of parliamentary parties to compare the narrow contexts.
## To identify lingustic and ideologic convergences and divergences over time and between the parliamentary parties the qualitative context is evaluated.
## In a previously done study a dictionary including relevant terms of citizenship discourse was deductively formulated.
## Related to the theoretical model, the dictionary contains terms referring to actors, issues, and norms of citizenship aspects.


## Three research questions will be followed:

## To what extent has the linguistic contextualization of the concept of citizenship in plenary debates of the Federal Republic of Germany on the federal level changed between 1996 and 2016?

## How is the concept of dual citizenship contextualised by parliamentary actors?

## To what extend do contextualisations of dual citizenship differ between parliamentary groups? Which arguments are represented?


